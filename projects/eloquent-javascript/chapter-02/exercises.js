// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(size) {
  //box to push into  
  var box = '';
    //rows
    for(let x = 0; x < size; x++){
      //collums
      for(let y = 0; y < size; y++){
        //if the sum of row index and column index is even, push space; if it odd, push #
        if((x + y) % 2 == 0){
          box += ' ';
         } else {box += '#';}
      }
      //line break inbetween rows
      box += '\n';  
    }
      console.log(box);
}

