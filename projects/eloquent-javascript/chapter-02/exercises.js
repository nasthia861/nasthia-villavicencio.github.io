function triangles(amount) {
//create variable to push #s into
  let line = '';
  //for loop to go from 0 to the amount parameter
  for( let x = 0; x < amount; x++){
    //each loop will add a # to line variable and then log it before it loops again
    line += '#';
    console.log(line);
  }
}

